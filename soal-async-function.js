function tambah(angka1, angka2, hasil) {
    hasil(angka1 + angka2)
}
function kurang(angka1, angka2, hasil) {
    hasil(angka1 - angka2)
}
function kali(angka1, angka2, hasil) {
    hasil(angka1 * angka2)
}
function bagi(angka1, angka2, hasil) {
    hasil(angka1 / angka2)
}
function pangkat(angka1, angka2, hasil) {
    hasil(angka1 ** angka2)
}

